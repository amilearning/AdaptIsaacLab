def stand_still_joint_deviation_l1(
    env, command_name: str, command_threshold: float = 0.06, asset_cfg: SceneEntityCfg = SceneEntityCfg("robot")
) -> torch.Tensor:
    """Penalize offsets from the default joint positions when the command is very small."""
    command = env.command_manager.get_command(command_name)
    # Penalize motion when command is nearly zero.
    return joint_deviation_l1(env, asset_cfg) * (torch.norm(command[:, :2], dim=1) < command_threshold)




def body_excessive_roll_pitch_exp_threshold(
    env,
    std: float = 1.0,
    threshold_deg: float = 70.0,
    asset_cfg: SceneEntityCfg = SceneEntityCfg("robot"),
) -> torch.Tensor:
    """Penalize excessive roll/pitch above threshold using exponential kernel."""
    asset: Articulation = env.scene[asset_cfg.name]
    t_roll, t_pitch, t_yaw = euler_xyz_from_quat(asset.data.root_pose_w[:, 3:])  # radians

    # convert threshold to radians
    threshold = torch.deg2rad(torch.tensor(threshold_deg, device=t_roll.device))

    # compute how much roll/pitch exceeds the threshold
    roll_excess = torch.clamp(torch.abs(t_roll) - threshold, min=0.0)
    pitch_excess = torch.clamp(torch.abs(t_pitch) - threshold, min=0.0)

    # combine roll and pitch excess
    total_excess = roll_excess + pitch_excess

    # exponential penalty: neutral when under threshold, decays once over threshold
    reward = torch.exp(-total_excess / std**2)
    return reward


